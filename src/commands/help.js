import { config } from '../config.js';

export function setupHelpCommand(bot) {
  bot.command('help', (ctx) => {
    ctx.reply(`👋 Привет! Я бот для создания сводок в групповых чатах.)
    \n\nВот что я умею:
    \n- /summary - Создать сводку из сообщений в буфере.
    \n- /last - Показать последнюю тему обсуждения.
    \n- /poll - Создать опрос на основе спорных моментов.
    \n- /meme - Создать мем на основе обсуждения.
    \n- /drama - Оценить уровень драмы и накал страстей 🎭
    \n- /quote - Цитата великих из чата 📜
    \n- /judge - Суд присяжных: кто прав, кто виноват ⚖️
    \n- /tasks - Показать все сохраненные задачи.
    \n- /stats - Показать статистику активности чата.
    \n- /askchat {текст} - Ответить на вопрос с учетом всей истории чата.
    \n- /s {текст} - Задать любой вопрос Gemini AI.
    \n- /clear - Очистить буфер сводки.
    \n- /remind {время} {текст} - Создать напоминание (например: /remind 20m обсудить это позже).
    \n- /sosal - Случайно выбрать пользователя 🍭
    \n- Просто напиши сообщение, и я буду собирать их в буфер (до ${config.buffer.maxSize} сообщений).
    \n\nЯ использую Google Gemini Pro для создания сводок. Буфер хранит до ${config.buffer.maxSize} сообщений, автоматически удаляя самые старые.`);
  });
}
